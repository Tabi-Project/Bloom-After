export async function fetchResources(filters = {}) {
  await new Promise(resolve => setTimeout(resolve, 600));

  let results = [...resources];

  if (filters.content_type) {
    results = results.filter(r => r.content_type === filters.content_type);
  }

  if (filters.q) {
    const q = filters.q.toLowerCase();
    results = results.filter(r =>
      r.title.toLowerCase().includes(q) ||
      r.summary.toLowerCase().includes(q)
    );
  }

  const page = filters.page || 1;
  const limit = filters.limit || 9;
  const startIndex = (page - 1) * limit;
  const endIndex = startIndex + limit;
  
  const paginatedData = results.slice(startIndex, endIndex);

  return {
    data: paginatedData,
    pagination: {
      totalResources: results.length,
      totalPages: Math.ceil(results.length / limit) || 1,
      currentPage: page,
      pageSize: limit,
      hasNextPage: endIndex < results.length,
      hasPrevPage: page > 1,
    }
  };
}

export async function fetchResourceById(id) {
  await new Promise(resolve => setTimeout(resolve, 400));
  return resources.find(r => r.id === Number(id)) || null;
}

export async function fetchRelatedResources(id, theme) {
  await new Promise(resolve => setTimeout(resolve, 400));
  return resources
    .filter(r => r.id !== Number(id) && r.theme === theme)
    .slice(0, 3);
}